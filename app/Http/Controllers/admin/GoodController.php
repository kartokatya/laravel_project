     */
    public function store(Request $request){

        $goods = new Good();
        $goods->name=$request->name;
        $goods->short_description=$request->short_description;
        $goods->description=$request->description;
        $goods->icon=$request->icon;
        $goods->save();
        //dd($request->all());
        return redirect('/admin/good');
    }

